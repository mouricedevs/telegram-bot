const fetch = (...args) => import('node-fetch').then(({ default: fetch }) => fetch(...args)); 

module.exports = {
    config: {
        name: "sspdf",
        author: "Gifted Tech",
        description: "Take a screenshot of a webpage or Google in pdf",
        category: "á´›á´á´ÊŸs",
        usage: ".sspdf <url>\n or\n/screenshot -g <text>",
        usePrefix: true
    },

    onStart: async function({ bot, chatId, args, msg }) {
        const p = '/'; 

        if (args.length === 0) {
            bot.sendMessage(chatId, `Invalid inputâš ï¸\nPlease use:\n${p}sspdf <url> \nor\n${p}sspdf  <link>.`);
            return;
        }

        let url;
        if (args[0] === '-g') {
            if (args.length < 2) {
                bot.sendMessage(chatId, `Invalid text input after -g tagâš ï¸\nPlease use:\n${p}sspdf  YourLink`);
                return;
            }
            const query = args.slice(1).join('+');
            url = `https://www.google.com/search?q=${query}&tbm=isch`;
        } else {
            url = args[0];
            if (!url.match(/^https?:\/\/.+$/)) {
                url = `https://${url}`;
            }
        }

        const apiURL = `https://api.vihangayt.com/tools/sspdf?url=${url}`;

        try {
            const res = await fetch(apiURL);
            if (!res.ok) {
                bot.sendMessage(chatId, `Invalid inputâš ï¸\nPlease use:\n${p}sspdf <url> \nor\n${p}sspdf  <link>.`);
                return;
            }

            bot.sendPhoto(chatId, apiURL, { caption: "Here is the screenshot in pdf format." });
        } catch (error) {
            console.error('[ERROR]', error);
            bot.sendMessage(chatId, 'An error occurred while processing the command.');
        }
    }
};


